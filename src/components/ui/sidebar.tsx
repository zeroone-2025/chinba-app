import * as React from "react"
import { cva, type VariantProps } from "class-variance-authority"
import { cn } from "@/lib/utils"

const sidebarVariants = cva(
  "flex h-full w-full flex-col overflow-hidden rounded-md bg-transparent text-sm",
  {
    variants: {
      variant: {
        default: "border",
        inset: "border border-sidebar-border bg-sidebar",
        sidebar: "border-r bg-sidebar/50",
      },
      collapsible: {
        none: "",
        icon: "",
        offcanvas: "",
      },
    },
    defaultVariants: {
      variant: "default",
      collapsible: "none",
    },
  }
)

export interface SidebarProps
  extends React.HTMLAttributes<HTMLDivElement>,
    VariantProps<typeof sidebarVariants> {}

const Sidebar = React.forwardRef<HTMLDivElement, SidebarProps>(
  ({ className, variant, collapsible, ...props }, ref) => (
    <div
      ref={ref}
      data-sidebar="sidebar"
      data-collapsible={collapsible === "none" ? undefined : collapsible}
      className={cn(sidebarVariants({ variant, collapsible }), className)}
      {...props}
    />
  )
)
Sidebar.displayName = "Sidebar"

const SidebarContent = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    data-sidebar="content"
    className={cn("flex min-h-0 flex-1 flex-col gap-2 overflow-auto group-data-[collapsible=icon]:overflow-hidden", className)}
    {...props}
  />
))
SidebarContent.displayName = "SidebarContent"

